grepInsensible() {
  grep -r -iF $1
}

gitConfigFunc() {
  git config --global core.editor "nvim"
  git config --global user.name "Yacine Idir"
  git config --global user.email "yacineidir1000@gmail.com"
}

downloadPack() {
  set -euo pipefail

  sudo pacman -Syu --noconfirm
  sudo pacman -S --noconfirm --needed \
    base-devel git gcc gdb make python python-pip nmap neovim
  
  if ! command -v paru >/dev/null 2>&1; then
    tmpdir="$(mktemp -d)"
    git clone https://aur.archlinux.org/paru.git "$tmpdir/paru"
    cd "$tmpdir/paru"
    makepkg -si --noconfirm
    cd ~
    rm -rf "$tmpdir"
  fi
  
  AUR_PACKAGES=(
    openstego
    pngcheck
    steghide
    stegseek
    stegsolve
    python-stegoveritas
    zsteg
    stegsnow-git
    stegify
  )
  
  declare -A PIP_FALLBACK=(
    [stegcracker]="stegcracker"
    [stepic]="stepic"
    [stegoveritas]="stegoveritas"
    [stegano]="stegano"
  )
  
  for pkg in "${AUR_PACKAGES[@]}"; do
    paru -S --noconfirm --needed "$pkg" || true
  done
  
  for pip_pkg in "${PIP_FALLBACK[@]}"; do
    python -m pip install --upgrade --user "$pip_pkg" || true
  done
  
  git clone https://github.com/LazyVim/starter ~/.config/nvim
  rm -rf ~/.config/nvim/.git
}

